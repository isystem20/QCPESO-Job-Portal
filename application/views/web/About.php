                            The Public Employment Service Office or PESO is a non-fee charging multi-employment <br>
                            service facility or entity established or accredited pursuant to Republic Act No. 8759 <br>
                            otherwise known as the PESO Act of 1999.<br>

                            Created by Executive Order No. 4 on February 9, 1989, the Industrial Relations
                            Office (IRO) is mandated <br>
                            "to develop and enhance the cooperation and understanding between management and labor <br>
                            towards industrial peace in Quezon City."<br>

                            This original mandate was broadened to include the promotion and sustenance of the City's <br>
                            Employment Program when, in September 3, 1992, the City Government signed a pertinent <br>
                            Memorandum of Agreement with the Department of Labor and Employment (DOLE). <br>Thus, the City's Public Employment Service Office (PESO) was born.<br></center><br>
